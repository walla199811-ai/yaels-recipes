// Helper function to get all ingredients as a flat array
export const getAllIngredients = (): string[] => {
  return hebrewIngredientsDatabase.reduce((acc, category) => {
    return acc.concat(category.items);
  }, [] as string[]);
};

// Helper function to search ingredients
export const searchIngredients = (query: string): string[] => {
  if (!query.trim()) return [];

  const normalizedQuery = query.trim().toLowerCase();
  const allIngredients = getAllIngredients();

  return allIngredients.filter(ingredient =>
    ingredient.toLowerCase().includes(normalizedQuery)
  ).slice(0, 10); // Limit to 10 results for performance
};

// Helper function to get suggestions based on partial input
export const getIngredientSuggestions = (input: string, limit = 5): string[] => {
  if (!input.trim()) return [];

  const normalizedInput = input.trim().toLowerCase();
  const allIngredients = getAllIngredients();

  // First, find exact matches at the beginning
  const startsWith = allIngredients.filter(ingredient =>
    ingredient.toLowerCase().startsWith(normalizedInput)
  ).slice(0, limit);

  // If we have enough matches, return them
  if (startsWith.length >= limit) {
    return startsWith;
  }

  // Otherwise, add partial matches
  const contains = allIngredients.filter(ingredient =>
    ingredient.toLowerCase().includes(normalizedInput) &&
    !ingredient.toLowerCase().startsWith(normalizedInput)
  ).slice(0, limit - startsWith.length);

  return [...startsWith, ...contains];
};